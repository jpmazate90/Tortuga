    public void cargarArchivo(){
        carga.leerArchivo(textArea);
    }
    
    public void guardarEjecutar(){
        if(archivo!=null){
             carga.guardarArchivo(archivo, textArea);
             Acciones.archivoEjecutar = archivo;
             Acciones.guardarEjecutar = true;
        }else{
            carga.guardarComoArchivo(textArea);
        }
        this.setVisible(false);
    }
    
    public void guardar(){
        if(archivo!=null){
            carga.guardarArchivo(archivo, textArea);
        }else{
            carga.guardarComoArchivo(textArea);
        }
        
        
        Acciones.archivoEjecutar = null;
        Acciones.guardarEjecutar = false;
        this.setVisible(false);
    }
    
    public void cancelar(){
        Acciones.archivoEjecutar = null;
        Acciones.guardarEjecutar = false;
        this.setVisible(false);
    }

